.set TABLE_BORDER_LEFT_BOTTOM,   TABLE_BORDER_RIGTH_TOP + ( 1 + RIGTH_SPACE + SCREEN_WIDTH*(TABLE_HEIGHT) + LEFT_SPACE + 1 + VERTICAL_HEADER_WIDTH )  * 2

.set TOP_HEADER_START,   BORDER_LEFT_TOP + (SCREEN_WIDTH + 1 + VERTICAL_HEADER_WIDTH + 1) * 2

